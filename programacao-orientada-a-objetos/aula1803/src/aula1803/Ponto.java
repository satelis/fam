public class Ponto {
    protected double x, y;
    
    public Ponto(double vX, double vY) {
        x = vX;
        y = vY;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
    
    
}
